package Aug3;
class Student {
    String name;
    int age;
    boolean gender;
    float grade;
    public Student(String name, int age, boolean gender, float grade){
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.grade = grade;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public boolean isGender() {
        return gender;
    }

    public float getGrade() {
        return grade;
    }
}

